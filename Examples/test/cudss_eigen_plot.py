df = pd.DataFrame(data)

# محاسبه ستون Speedup برای هر ردیف
def get_speedup(row):
    if row['Solver'] == 'Eigen':
        return row['Speedup (Eigen)']
    else:
        return row['Speedup (cuDSS)']

df['Speedup'] = df.apply(get_speedup, axis=1)

# تفکیک داده‌ها بر اساس Solver
eigen = df[df['Solver'] == 'Eigen']
cuDSS = df[df['Solver'] == 'cuDSS']

# نمودار 1: محور افقی # Poses، محور عمودی Speedup
plt.figure(figsize=(8,5))
plt.scatter(eigen['# Poses'], eigen['Speedup'], color='blue', label='Eigen')
plt.scatter(cuDSS['# Poses'], cuDSS['Speedup'], color='red', label='cuDSS')
plt.xlabel('# Poses')
plt.ylabel('Speedup')
plt.title('Speedup vs # Poses')
plt.legend()
plt.grid(True)
plt.show()

# نمودار 2: محور افقی # Edges، محور عمودی Speedup
plt.figure(figsize=(8,5))
plt.scatter(eigen['# Edges'], eigen['Speedup'], color='blue', label='Eigen')
plt.scatter(cuDSS['# Edges'], cuDSS['Speedup'], color='red', label='cuDSS')
plt.xlabel('# Edges')
plt.ylabel('Speedup')
plt.title('Speedup vs # Edges')
plt.legend()
plt.grid(True)
plt.show()
